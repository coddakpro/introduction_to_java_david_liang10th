package javaCodeLesson1;

import java.util.Arrays;

public class ConvertFeet {
    public static void main(String[] args) {
        int number = 10;
        for (int i = 0; i <= number; ++i) {
            for (int j = 0; j <= number; ++j) {
            }
            System.out.print("*");
        }
        System.out.println();
    }
}